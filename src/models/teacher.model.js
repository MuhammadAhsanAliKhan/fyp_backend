const mongoose = require("mongoose");

const teacherSchema = new mongoose.Schema({
    email: {
        type: String,
        required: true,
        unique: true,
    },
    password: {
        type: String,
        required: true,
    },
    name: {
        type: String,
        required: true,
    },
    age: {
        type: Number,
        required: true,
    },
});

const Teacher = mongoose.model("Teacher", teacherSchema, "Teachers");

module.exports = Teacher;
